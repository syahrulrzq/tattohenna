  function readFile(input) {
    if (input.files && input.files[0]) {
      var reader = new FileReader();

      reader.onload = function(e){
        var imageURL = e.target.result;
        // var pngimg = imageURL.toDataURL('image/png');
        // var str = imageURL.replace(['image/png',],'')
        $('#image').val('');
        $('#image').val(imageURL);
        var mime = document.getElementById('exampleInputFile').files[0].type;
        // var str = mime.replace('');
        var realmime = mime.replace('image/','');
        $('#mime').val('');
        $('#mime').val(realmime);
      }
        reader.readAsDataURL(input.files[0]);
    }
  }
